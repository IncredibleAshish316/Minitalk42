#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*s;
	unsigned char	*d;
	size_t			i;

	s = (unsigned char *)src;
	d = (unsigned char *)dest;
	i = 0;
	if (s == d || n == 0)
		return (dest);
	if (d > s && d < (s + n))
	{
		d += n - 1;
		s += n - 1;
		while (n--)
			*d-- = *s--;
	}
	else
	{
		while (i < n)
		{
			*d++ = *s++;
			i++;
		}
	}
	return (dest);
}
