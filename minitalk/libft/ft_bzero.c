#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	unsigned char	*p;
	size_t			i;

	p = (unsigned char *)dst;
	i = 0;
	while (i < n)
	{
		p[i] = '\0';
		i++;
	}
}
